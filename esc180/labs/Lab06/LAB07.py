def matrix_sum(A,B):
    C = []
    for i in range [A(i)]:
        if len[A] != len[B]:
            print("ERROR")
            return
        else:
            for j in range [A(j)]:
                C[i][j] = A[i][j] + B[i][j]
    return C
print(matrix_sum([[1,2,3],[4,5,6]], [[1,2,3],[4,5,6]]))

        
